class UnknownSymbol(Exception):
    """
    Thrown when the parser meets a symbol that it does not know about
    """

    def __init__(self, symbol):
        self._symbol = symbol

    def __repr__(self):
        return "Unknown symbol '{}'".format(self._symbol)
